package com.math.clockangle.model.clock;

public class Clock {
	
	private int hour;
	
	private int minute;
		
	public int getHour() {
		return hour;
	}

	public void setHour(int hour) {
		this.hour = hour;
	}

	public int getMinute() {
		return minute;
	}

	public void setMinute(int minute) {
		this.minute = minute;
	}
	
}
